#!/usr/bin/env node
// mcp-scaffold.mjs — Generate all MCP server files for an A2A agent
//
// Usage:
//   node mcp-scaffold.mjs --agent-root /path/to/agent --slug sin-myagent \
//     [--namespace sin_myagent] [--tools '<json>'] [--env-vars '<json>'] [--format json]
//
// Generates:
//   src/mcp-server.ts, mcp-config.json, clients/opencode-mcp.json
//   Patches src/cli.ts if serve-mcp command is missing

import { resolve, join } from 'path';
import {
  parseArgs, requireArg, slugToNamespace,
  readText, writeText, readJson, writeJson,
  ensureDir, fileExists, getDefaultTools,
  mapTypeToZod, ok, warn, fail, info, jsonOutput,
} from './_mcp-lib.mjs';

const args = parseArgs();
const agentRoot = resolve(requireArg(args, 'agent-root', 'Absolute path to agent'));
const slug = requireArg(args, 'slug', 'MCP slug e.g. sin-myagent');
const namespace = args.namespace || slugToNamespace(slug);
const format = args.format || 'text';

// Parse tool definitions
let userTools = [];
if (args.tools) {
  try {
    userTools = JSON.parse(args.tools);
    if (!Array.isArray(userTools)) throw new Error('tools must be an array');
  } catch (e) {
    fail(`Invalid --tools JSON: ${e.message}`);
    process.exit(1);
  }
}

// Parse env vars
let envVars = {};
if (args['env-vars']) {
  try {
    envVars = JSON.parse(args['env-vars']);
  } catch (e) {
    fail(`Invalid --env-vars JSON: ${e.message}`);
    process.exit(1);
  }
}

// ── Merge default + user tools ───────────────────────────────
const defaultTools = getDefaultTools(namespace);
const allTools = [
  ...defaultTools,
  ...userTools.map(t => ({
    name: t.name.startsWith(namespace) ? t.name : `${namespace}_${t.name}`,
    description: t.description || `Execute ${t.name}`,
    params: t.params || {},
    action: t.action || `${namespace.replace(/_/g, '.')}.${t.name}`,
  })),
];

// ── Generate src/mcp-server.ts ───────────────────────────────
function generateMcpServerTs() {
  const toolRegistrations = allTools.map(tool => {
    const hasParams = Object.keys(tool.params).length > 0;
    const paramEntries = Object.entries(tool.params);

    let inputSchemaStr = '{}';
    if (hasParams) {
      const zodFields = paramEntries
        .map(([k, v]) => `      ${k}: ${mapTypeToZod(v)},`)
        .join('\n');
      inputSchemaStr = `{\n${zodFields}\n    }`;
    }

    // Build handler
    const handlerParams = hasParams ? 'args' : '';
    const executeArgs = hasParams
      ? `{ action: '${tool.action}', ...args }`
      : `{ action: '${tool.action}' }`;

    return `  server.registerTool(
    '${tool.name}',
    { description: '${tool.description.replace(/'/g, "\\'")}' },
    async (${handlerParams}) => {
      const result = await executeAction(${executeArgs}, agentConfig);
      return {
        content: [{ type: 'text' as const, text: JSON.stringify(result, null, 2) }],
      };
    }
  );`;
  }).join('\n\n');

  return `// MCP Server for ${slug}
// Auto-generated by /create-a2a-mcp skill — do not edit header
// Production pattern: McpServer.registerTool()

import { McpServer } from '@modelcontextprotocol/sdk/server/mcp.js';
import { StdioServerTransport } from '@modelcontextprotocol/sdk/server/stdio.js';
import { z } from 'zod';
import { executeAction } from './runtime.js';
import { agentConfig } from './metadata.js';

export async function startMcpServer(): Promise<void> {
  const server = new McpServer({
    name: '${slug}',
    version: agentConfig.version || '0.1.0',
  });

${toolRegistrations}

  const transport = new StdioServerTransport();
  await server.connect(transport);
}
`;
}

// ── Generate mcp-config.json ─────────────────────────────────
function generateMcpConfig() {
  const config = {
    mcpServers: {
      [slug]: {
        command: 'node',
        args: ['dist/src/cli.js', 'serve-mcp'],
      },
    },
  };
  if (Object.keys(envVars).length > 0) {
    config.mcpServers[slug].env = envVars;
  }
  return config;
}

// ── Generate clients/opencode-mcp.json ───────────────────────
function generateClientConfig() {
  return {
    mcpServers: {
      [slug]: {
        command: 'node',
        args: ['dist/src/cli.js', 'serve-mcp'],
      },
    },
  };
}

// ── Patch cli.ts ─────────────────────────────────────────────
function patchCliTs() {
  const cliPath = join(agentRoot, 'src/cli.ts');
  const content = readText(cliPath);
  if (!content) {
    warn(`src/cli.ts not found at ${cliPath} — skipping patch`);
    return false;
  }
  if (content.includes('serve-mcp')) {
    info('src/cli.ts already has serve-mcp command');
    return true;
  }

  // Find the right insertion point — after serve-a2a or after command parsing
  const serveMcpBlock = `
  if (command === 'serve-mcp') {
    const { startMcpServer } = await import('./mcp-server.js');
    await startMcpServer();
    return;
  }
`;

  let patched;
  if (content.includes("if (command === 'serve-a2a')")) {
    // Insert after serve-a2a block
    const marker = content.indexOf("if (command === 'serve-a2a')");
    const blockEnd = content.indexOf('}', content.indexOf('}', marker) + 1) + 1;
    patched = content.slice(0, blockEnd) + '\n' + serveMcpBlock + content.slice(blockEnd);
  } else if (content.includes("if (command === 'print-card')")) {
    // Insert before print-card
    const marker = content.indexOf("if (command === 'print-card')");
    patched = content.slice(0, marker) + serveMcpBlock + '\n  ' + content.slice(marker);
  } else {
    // Fallback: append before last closing brace
    const lastBrace = content.lastIndexOf('}');
    patched = content.slice(0, lastBrace) + serveMcpBlock + content.slice(lastBrace);
  }

  writeText(cliPath, patched);
  return true;
}

// ── Execute ──────────────────────────────────────────────────
const results = { files: [], warnings: [], errors: [] };

// 1. Write mcp-server.ts
const mcpServerPath = join(agentRoot, 'src/mcp-server.ts');
try {
  ensureDir(join(agentRoot, 'src'));
  writeText(mcpServerPath, generateMcpServerTs());
  results.files.push(mcpServerPath);
  ok(`Generated ${mcpServerPath}`);
} catch (e) {
  fail(`Failed to write mcp-server.ts: ${e.message}`);
  results.errors.push(mcpServerPath);
}

// 2. Write mcp-config.json
const mcpConfigPath = join(agentRoot, 'mcp-config.json');
try {
  writeJson(mcpConfigPath, generateMcpConfig());
  results.files.push(mcpConfigPath);
  ok(`Generated ${mcpConfigPath}`);
} catch (e) {
  fail(`Failed to write mcp-config.json: ${e.message}`);
  results.errors.push(mcpConfigPath);
}

// 3. Write clients/opencode-mcp.json
const clientConfigPath = join(agentRoot, 'clients/opencode-mcp.json');
try {
  ensureDir(join(agentRoot, 'clients'));
  writeJson(clientConfigPath, generateClientConfig());
  results.files.push(clientConfigPath);
  ok(`Generated ${clientConfigPath}`);
} catch (e) {
  fail(`Failed to write clients/opencode-mcp.json: ${e.message}`);
  results.errors.push(clientConfigPath);
}

// 4. Patch cli.ts
const cliPatched = patchCliTs();
if (cliPatched) {
  results.files.push(join(agentRoot, 'src/cli.ts'));
}

// ── Summary ──────────────────────────────────────────────────
info(`Scaffolded ${allTools.length} tools (${defaultTools.length} default + ${userTools.length} custom)`);
info(`Tools: ${allTools.map(t => t.name).join(', ')}`);

if (format === 'json') {
  jsonOutput({
    success: results.errors.length === 0,
    slug,
    namespace,
    toolCount: allTools.length,
    files: results.files,
    warnings: results.warnings,
    errors: results.errors,
  });
} else {
  console.log(`\n📦 MCP scaffold complete for ${slug}`);
  console.log(`   ${results.files.length} files generated, ${results.errors.length} errors`);
}

process.exit(results.errors.length > 0 ? 1 : 0);
