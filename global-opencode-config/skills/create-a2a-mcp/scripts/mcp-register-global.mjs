#!/usr/bin/env node
// mcp-register-global.mjs — Register an MCP in global opencode.json
//
// Usage:
//   node mcp-register-global.mjs --slug sin-myagent --agent-root /path/to/agent \
//     [--bin-wrapper] [--env-vars '<json>'] [--format json]
//
// Actions:
//   1. Adds MCP entry to ~/.config/opencode/opencode.json under mcp section
//   2. Optionally creates bin wrapper at ~/dev/SIN-Solver/bin/sin-<name>

import { resolve, join } from 'path';
import { writeFileSync, chmodSync } from 'fs';
import {
  parseArgs, requireArg, readJson, writeJson,
  fileExists, ensureDir, ok, warn, fail, info, jsonOutput,
  OPENCODE_CONFIG, SIN_SOLVER_BIN,
} from './_mcp-lib.mjs';

const args = parseArgs();
const slug = requireArg(args, 'slug', 'MCP slug e.g. sin-myagent');
const agentRoot = resolve(requireArg(args, 'agent-root', 'Absolute path to agent'));
const createBinWrapper = args['bin-wrapper'] === true || args['bin-wrapper'] === 'true';
const format = args.format || 'text';

// Parse env vars
let envVars = {};
if (args['env-vars']) {
  try {
    envVars = JSON.parse(args['env-vars']);
  } catch (e) {
    fail(`Invalid --env-vars JSON: ${e.message}`);
    process.exit(1);
  }
}

const results = { actions: [], warnings: [], errors: [] };

// ── 1. Register in opencode.json ─────────────────────────────
const config = readJson(OPENCODE_CONFIG);
if (!config) {
  fail(`Cannot read ${OPENCODE_CONFIG}`);
  process.exit(1);
}

if (!config.mcp) config.mcp = {};

// Check if already registered
if (config.mcp[slug]) {
  warn(`MCP "${slug}" already exists in opencode.json — updating`);
}

// Determine command
let command;
const binPath = join(SIN_SOLVER_BIN, slug);

if (createBinWrapper) {
  command = [binPath, 'serve-mcp'];
} else {
  command = [
    'node',
    join(agentRoot, 'dist/src/cli.js'),
    'serve-mcp',
  ];
}

// Build MCP entry
const mcpEntry = {
  type: 'local',
  command,
  enabled: true,
};

if (Object.keys(envVars).length > 0) {
  mcpEntry.environment = envVars;
}

config.mcp[slug] = mcpEntry;

try {
  writeJson(OPENCODE_CONFIG, config);
  ok(`Registered "${slug}" in ${OPENCODE_CONFIG}`);
  results.actions.push(`registered:${OPENCODE_CONFIG}`);
} catch (e) {
  fail(`Failed to write opencode.json: ${e.message}`);
  results.errors.push(`opencode.json:${e.message}`);
}

// ── 2. Create bin wrapper (optional) ─────────────────────────
if (createBinWrapper) {
  ensureDir(SIN_SOLVER_BIN);
  const wrapper = `#!/usr/bin/env bash
# Bin wrapper for ${slug} MCP — auto-generated by /create-a2a-mcp
exec node "${join(agentRoot, 'dist/src/cli.js')}" "$@"
`;
  try {
    writeFileSync(binPath, wrapper, 'utf8');
    chmodSync(binPath, 0o755);
    ok(`Created bin wrapper: ${binPath}`);
    results.actions.push(`bin-wrapper:${binPath}`);
  } catch (e) {
    fail(`Failed to create bin wrapper: ${e.message}`);
    results.errors.push(`bin-wrapper:${e.message}`);
  }
}

// ── Summary ──────────────────────────────────────────────────
if (format === 'json') {
  jsonOutput({
    success: results.errors.length === 0,
    slug,
    actions: results.actions,
    warnings: results.warnings,
    errors: results.errors,
  });
} else {
  console.log(`\n🔗 Global registration complete for ${slug}`);
}

process.exit(results.errors.length > 0 ? 1 : 0);
